
const db = require("../models");
const RoomAmenities = db.roomAmenities;
const Op = db.Sequelize.Op;

exports.create = (req, res) => {
    // Validate request
    if (!req.body.title) {
        res.status(400).send({
            message: "Content can not be empty!"
        });
        return;
    }
    const roomAmenities = {
        amenities: req.body.amenities,

    };
    RoomAmenities.create(roomAmenities)
        .then(data => {
            res.send(data);
        })
        .catch(err => {
            res.status(500).send({
                message:
                    err.message || "Some error occurred while creating the Tutorial."
            });
        });
};

